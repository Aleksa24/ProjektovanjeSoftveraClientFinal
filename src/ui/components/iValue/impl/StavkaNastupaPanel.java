    public void popuniTabelu(List<StavkaNastupa> listaStavkiNastupa) {
        tabelaStavki.setModel(new StavkaNastupaTableModel(listaStavkiNastupa));
        tabelaStavki.getColumnModel().getColumn(0).setPreferredWidth(25);
        tabelaStavki.getColumnModel().getColumn(1).setPreferredWidth(100);
        tabelaStavki.getColumnModel().getColumn(2).setPreferredWidth(50);
        tabelaStavki.getColumnModel().getColumn(3).setPreferredWidth(150);
    }
