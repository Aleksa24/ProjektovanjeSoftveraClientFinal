    private void btnUbaciStavkuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUbaciStavkuActionPerformed
        try {
            //treba da uzme vrednosti iz polja, da napravi stavku i da je ubaci u tabelu
            ((StavkaNastupaTableModel) tabelaStavki.getModel())
                    .ubaciStavku(new StavkaNastupa()
                            .appendVreme((Date) this.vremePanel.getValue())
                            .appendTrajanje(Long.valueOf((String) this.trajanjePanel.getValue()))
                            .appendIzvodjac((DomainObject) this.izvodjacPanel.getValue()));
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnUbaciStavkuActionPerformed

    private void btnIzbaciStavkuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIzbaciStavkuActionPerformed
        //uzima iz tabele selektovanu stavku i izbacuje je iz modela
        int selectedRow = tabelaStavki.getSelectedRow();
        if (selectedRow == -1) {
            return;
        }
        StavkaNastupa s = ((StavkaNastupaTableModel) tabelaStavki.getModel())
                .getStavkaNastupa(selectedRow);
        ((StavkaNastupaTableModel) tabelaStavki.getModel())
                .izbaciStavku(s);
        
    }//GEN-LAST:event_btnIzbaciStavkuActionPerformed

    /**
     * uzima listuStavki iz tabele
     *
     * @return
     * @throws Exception
     */
    @Override
    public Object getValue() throws Exception {
        validator.validate();
        return ((StavkaNastupaTableModel) tabelaStavki.getModel()).getListaStavki();
    }

    /**
     * ubacuje u tabelu listu stavki
     *
     * @param value
     */
    @Override
    public void setValue(Object value) {
        this.tabelaStavki.setModel(new StavkaNastupaTableModel((List<StavkaNastupa>) value));
    }

    /**
     * ubacuje u combo listu izvodjaca
     *
     * @param initValue
     */
    @Override
    public void inicialize(Object initValue) {
        this.izvodjacPanel.inicialize(initValue);
    }
    
    @Override
    public void setValidator(VrednosniValidator validator) {
        this.validator = validator;
    }

    //izvodjacPanel tabelaStavki trajanjePanel vremePanel
    private void preparePanel() {
        this.vremePanel.getJlabText().setText("Vreme:");
        this.vremePanel.getJlabError().setText("");
        this.vremePanel.inicialize(1996);
        
        
        this.trajanjePanel.getJlabText().setText("Trajanje:");
        this.trajanjePanel.getJlabError().setText("");
        this.trajanjePanel.getJtxtInput().setText("");
        this.trajanjePanel.setValidator(new VrednosniValidatorPozitivanBroj(trajanjePanel));
        
        this.izvodjacPanel.getJlabText().setText("Izvodjaci/Bendovi:");
        this.izvodjacPanel.setState("singleTake");
        
        popuniTabelu(new ArrayList<StavkaNastupa>());
    }

